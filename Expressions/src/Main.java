
public class Main {

	public static void main(String[] args) {
		
		// expression = operands & operators 
		// operands = values, variables, numbers, quantity 
		// operators = + - * / %
		
		double friends = 10;
		
		
		friends = (double) friends / 3;
		
		friends++;
		friends--;
		
		
		friends = friends % 2;
		
		friends = friends * 34;
				
		System.out.println(friends);

	}

}
